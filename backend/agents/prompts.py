def game_filtered_system_addendum(
    fqn_by_logical: dict[str, str],
    filter_countries: list[str],
    filter_app_versions: list[str],
) -> str:
    """Appended to the game system prompt when a session slice is active.

    A filtered run rebuilds ``game_user_journeys`` and ``game_levels`` from a
    subset of sessions (the country / app_version selection from the UI). The
    suffixed table names are stitched into the prompt so the agent only ever
    references the slice's own tables; the unsuffixed base tables would mix in
    sessions outside the filter and produce wrong totals.
    """
    c_desc = (
        ", ".join(filter_countries)
        if filter_countries
        else "all countries (no country restriction)"
    )
    v_desc = (
        ", ".join(filter_app_versions)
        if filter_app_versions
        else "all app versions (no version restriction)"
    )
    return (
        "\n\n---\n"
        "FILTERED DEEP SCAN (country / app_version slice)\n"
        f"- Active filter: countries: {c_desc}; app_versions: {v_desc}.\n"
        "- In the **second italic line** under the report H2, the *Session filter:* line "
        f"must name these selections verbatim (e.g. list each chosen country and each "
        f"chosen app version). Do not write \"none\" for this run — a slice is active.\n"
        "- Rule: at **session** level, include a session if its `country` matches the "
        "selected country list when that list is non-empty, **and** its `app_version` "
        "matches the selected version list when that list is non-empty. Empty list on "
        "either side means no restriction on that dimension. `game_user_journeys` and "
        "`game_levels` are **rebuilt** from this slice only — do not query the "
        "unsuffixed base tables for those rollups on this run.\n"
        "- Use **only** these four fully-qualified table names in SQL:\n"
        f"  * sessions: {fqn_by_logical.get('game_sessions', '')}\n"
        f"  * attempts: {fqn_by_logical.get('game_level_attempts', '')}\n"
        f"  * user journeys: {fqn_by_logical.get('game_user_journeys', '')}\n"
        f"  * levels: {fqn_by_logical.get('game_levels', '')}\n"
        "- `game_events_test` is not available for this filtered run. Do not query it.\n"
    )


